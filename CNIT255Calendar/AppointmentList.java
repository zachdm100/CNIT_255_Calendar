    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        // Delete an appointment
        String titleToCompare = jList1.getSelectedValue();
        System.out.println("Deleting " + titleToCompare);
        for(ApptItem a: appts)
        {
            if((a.title.compareTo(titleToCompare) == 0) && 
                (a.day.compareTo(String.valueOf(thisDay)) == 0) && 
                (a.month.compareTo(thisMonth) == 0) && 
                (a.year.compareTo(String.valueOf(thisYear))==0))
                {
                    System.out.println("FOUND-DELETE" + "\nDay: " + a.day + "\nMonth: " + a.month + "\nYear: " + a.year);
                    appts.remove(a);
                    UpdateList();
                    break;
                }
            
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        // Add a new appointment from the list dialog - use the parent CalendarGUI
        Appointment apptForm = new Appointment(myParent, true, thisDay, thisMonth, thisYear);
        apptForm.setVisible(true);
            
        if(apptForm.Saved)
        {
            // Create new entry to the appointments list
            appts.add(new ApptItem(apptForm.title, Integer.toString(thisDay), thisMonth, 
                    Integer.toString(thisYear))); 
            UpdateList();                   
        }
                
        apptForm.dispose(); // delete the dialog obj
        
    }//GEN-LAST:event_btnAddActionPerformed

