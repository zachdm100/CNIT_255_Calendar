    private void UpdateList()
    {
        listModel.clear();
        for(ApptItem a: appts)
        {
            if(Integer.parseInt(a.day) == thisDay) 
              if(a.month.compareTo(thisMonth) == 0)
                if(Integer.parseInt(a.year) == thisYear)
                   listModel.addElement(a.title);
                    
        }
        
    }
    
    
